////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import { MESSAGES } from '../constants';
import MessageService from '../services/message';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * View
 * @class
 */
class View {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  _messageService;
  _entities;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * View
   * @constructor
   */
  constructor() {
    this._messageService = MessageService.create();
    this._messageService.subscribe(MESSAGES.PROPERTY_CHANGE, (message) => this._handlePropertyChange(message));
    this._entities = [];
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
  _findEntity(id) { // eslint-disable-line id-length
    return this._entities.find((entity) => {
      return entity.id === id;
    });
  }

  _handlePropertyChange(message) {
    const ENTITY = this._findEntity(message.recipient);

    ENTITY.applyEvent(message);
  }
  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   * @returns {View}
   */
  static create() {
    return new View();
  }

}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default View;
