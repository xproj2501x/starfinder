////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import AjaxService from '../services/ajax';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const AJAX_SERVICE = AjaxService.create();
////////////////////////////////////////////////////////////////////////////////
// Function
////////////////////////////////////////////////////////////////////////////////
function Template(file) {
  return function(target) {
    AJAX_SERVICE.get({
      url: file,
      headers: {
        'Content-Type': 'text/html',
        'Accept': 'text/html'
      }
    }).then((response) => {
        console.log(response.responseText);
      }).catch((err) => {
      });
  };
}
////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Template;
